/*
   6-bit character set:
     0:     null
     1-9:   ASCII 38-47  (&'()*+,-./)
     10-19: ASCII '0'-'9'
     20-46: ASCII 'A'-'Z'
     47:    ASCII ' '
*/

static void callsign_to_ota(const char *in, char *out, int maxLen) {
    int o = 0;
    for (int i = 0; i < maxLen && in[i]; i++) {
        char c = in[i];
        if (c >= 38 && c <= 47)       out[o++] = c - 37;
        else if (c >= '0' && c <= '9') out[o++] = c - '0' + 10;
        else if (c >= 'A' && c <= 'Z') out[o++] = c - 'A' + 20;
        else if (c >= 'a' && c <= 'z') out[o++] = toupper(c) - 'A' + 20;
        else if (c == ' ')             out[o++] = 47;
    }
    out[o] = 0;
}

static void ota_to_callsign(const char *in, char *out, int maxLen) {
    int o = 0;
    for (int i = 0; i < maxLen && in[i]; i++) {
        unsigned char c = (unsigned char)in[i];
        if (c >= 1 && c <= 9)        out[o++] = c + 37;
        else if (c >= 10 && c <= 19)  out[o++] = c - 10 + '0';
        else if (c >= 20 && c <= 46)  out[o++] = c - 20 + 'A';
        else if (c == 47)             out[o++] = ' ';
    }
    out[o] = 0;
}

static unsigned char crc8(const char *data, int len) {
    unsigned char generator = 0x1D;
    unsigned char crc = 0x00;
    for (int i = 0; i < len; i++) {
        if (data[i] == 0) break;
        crc ^= (unsigned char)data[i];
        for (int b = 0; b < 8; b++)
            crc = (crc & 0x80) ? (unsigned char)((crc << 1) ^ generator) : (crc << 1);
    }
    return crc;
}

