void rade_text_generate_tx_string(rade_text_t ptr, const char *str,
                                  int strlength, float *syms, int symSize) {
    rade_text_impl_t *impl = (rade_text_impl_t *)ptr;

    /* Build raw payload: [CRC-8][6-bit OTA chars...] */
    char tmp[RADE_TEXT_MAX_RAW + 1];
    memset(tmp, 0, sizeof(tmp));

    int maxChars = (strlength < RADE_TEXT_MAX_LENGTH) ? strlength : RADE_TEXT_MAX_LENGTH;
    callsign_to_ota(str, &tmp[RADE_TEXT_CRC_LENGTH], maxChars);

    int txtLen = (int)strlen(&tmp[RADE_TEXT_CRC_LENGTH]);
    if (txtLen > RADE_TEXT_MAX_LENGTH) txtLen = RADE_TEXT_MAX_LENGTH;
    tmp[0] = (char)crc8(&tmp[RADE_TEXT_CRC_LENGTH], txtLen);

    /* Pack into 56 information bits */
    unsigned char ibits[LDPC_NDATA];
    unsigned char pbits[LDPC_NPARITY];
    memset(ibits, 0, LDPC_NDATA);
    memset(pbits, 0, LDPC_NPARITY);

    /* First 8 bits: CRC byte */
    for (int b = 0; b < 8; b++)
        if (tmp[0] & (1 << b)) ibits[b] = 1;

    /* Remaining 48 bits: 8 × 6-bit characters */
    for (int b = 8; b < LDPC_NDATA; b++) {
        int fromCrc = b - 8;
        unsigned char byte = (unsigned char)tmp[RADE_TEXT_CRC_LENGTH + fromCrc / 6];
        if (byte & (1 << (fromCrc % 6)))
            ibits[b] = 1;
    }

    /* LDPC encode */
    ldpc_encode(ibits, pbits);

    /* Combine info + parity, then interleave */
    char tmpbits[LDPC_CODELENGTH];
    memcpy(&tmpbits[0], ibits, LDPC_NDATA);
    memcpy(&tmpbits[LDPC_NDATA], pbits, LDPC_NPARITY);

    gp_interleave_bits(impl->tx_text, tmpbits);

    /* Map to QPSK symbols (I,Q float pairs) */
    for (int i = 0; i < GP_N; i++) {
        char b0 = impl->tx_text[2 * i];
        char b1 = impl->tx_text[2 * i + 1];
        /* Same mapping as freedv-gui / codec2:
           00 → (+1, 0), 01 → (0, +1), 10 → (0, -1), 11 → (-1, 0) */
        if      (b0 == 0 && b1 == 0) { syms[2*i] =  1; syms[2*i+1] =  0; }
        else if (b0 == 0 && b1 == 1) { syms[2*i] =  0; syms[2*i+1] =  1; }
        else if (b0 == 1 && b1 == 0) { syms[2*i] =  0; syms[2*i+1] = -1; }
        else                          { syms[2*i] = -1; syms[2*i+1] =  0; }
    }

    /* Pad remaining symbols with known sequence */
    for (int i = LDPC_CODELENGTH; i < symSize; i++)
        syms[i] = (i % 2) ? 0.0f : 1.0f;
}

void rade_text_rx(rade_text_t ptr, float *syms, int symSize) {
    rade_text_impl_t *impl = (rade_text_impl_t *)ptr;
    int nsyms = LDPC_CODELENGTH / 2;   /* 56 QPSK symbols */

    /* Deinterleave received QPSK symbols */
    rt_comp deinterleaved[GP_N];
    gp_deinterleave_comp(deinterleaved, (rt_comp *)syms);

    /* Calculate RMS amplitude */
    float rms = 0.0f;
    for (int i = 0; i < nsyms; i++)
        rms += deinterleaved[i].real * deinterleaved[i].real
             + deinterleaved[i].imag * deinterleaved[i].imag;
    /* Include unused symbols in RMS denominator for consistency with freedv-gui */
    int totalSyms = symSize / 2;
    for (int i = nsyms; i < totalSyms; i++) {
        float sr = syms[2 * i], si = syms[2 * i + 1];
        rms += sr * sr + si * si;
    }
    rms = sqrtf(rms / (totalSyms > 0 ? totalSyms : 1));

    /* Build amplitude array (uniform = RMS) */
    float amps[GP_N];
    for (int i = 0; i < nsyms; i++) amps[i] = rms;

    /* Convert symbols to LLRs */
    float llr[LDPC_CODELENGTH];
    float EsNo = 3.0f;  /* constant from freedv_700.c */
    symbols_to_llrs(llr, deinterleaved, amps, EsNo, rms, nsyms);

    /* LDPC decode */
    uint8_t output[LDPC_CODELENGTH];
    int parityCheckCount = 0;
    ldpc_decode(output, llr, &parityCheckCount);

    /* Check BER estimate */
    float ber_est = (float)(LDPC_NPARITY - parityCheckCount) / LDPC_NPARITY;
    if (ber_est >= 0.2f) return;  /* too many errors */

    /* Extract raw bytes from decoded bits */
    char rawStr[RADE_TEXT_MAX_RAW + 1];
    memset(rawStr, 0, sizeof(rawStr));

    /* First 8 bits → CRC byte */
    for (int b = 0; b < 8; b++)
        if (output[b]) rawStr[0] |= (1 << b);

    /* Remaining bits → 6-bit OTA characters */
    for (int b = 8; b < LDPC_NDATA; b++) {
        int fromCrc = b - 8;
        if (output[b])
            rawStr[RADE_TEXT_CRC_LENGTH + fromCrc / 6] |= (1 << (fromCrc % 6));
    }

    /* Convert OTA to ASCII */
    char decoded[RADE_TEXT_MAX_RAW + 1];
    memset(decoded, 0, sizeof(decoded));
    decoded[0] = rawStr[0];  /* CRC */
    ota_to_callsign(&rawStr[RADE_TEXT_CRC_LENGTH], &decoded[RADE_TEXT_CRC_LENGTH],
                    RADE_TEXT_MAX_LENGTH);

    /* Verify CRC */
    unsigned char rxCRC  = (unsigned char)decoded[0];
    unsigned char calcCRC = crc8(&rawStr[RADE_TEXT_CRC_LENGTH], RADE_TEXT_MAX_LENGTH);

    if (rxCRC == calcCRC && impl->text_rx_callback) {
        const char *txt = &decoded[RADE_TEXT_CRC_LENGTH];
        impl->text_rx_callback(ptr, txt, (int)strlen(txt), impl->callback_state);
    }
}
